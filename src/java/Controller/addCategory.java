    public boolean isvalid(String data) {

        return data != null && !data.isEmpty();
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        Part filePart = request.getPart("photo");
        String name = request.getParameter("name");

        InputStream input = null;
        Categories_model categor = new Categories_model();
        try {

            input = filePart.getInputStream();
            if (filePart != null &&!filePart.getName().isEmpty()&& isvalid(name)) {
                if (categor.insertCategory(name, input)) {
                    response.sendRedirect("admin/Categories.jsp?state=inserted");
                } else {
                    response.sendRedirect("admin/Categories.jsp?state=err");
                }
            } else {
                response.sendRedirect("admin/Categories.jsp?state=un");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

